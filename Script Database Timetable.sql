/*1*/
CREATE TABLE faculties (
  id serial PRIMARY KEY,
  short_name_of_faculty text,
  full_name_of_faculty text
);

/*2*/
CREATE TABLE pulpits (
  id serial PRIMARY KEY,
  short_name_of_pulpit text,
  full_name_of_pulpit text,
  id_of_faculty integer REFERENCES faculties(id)
);

/*3*/
CREATE TABLE specialties (
  code_of_specialty text PRIMARY KEY,
  short_name_of_specialty text,
  full_name_of_specialty text,
  id_of_faculty integer REFERENCES faculties(id)
);

/*4*/
CREATE TABLE teachers (
  id serial PRIMARY KEY,
  last_name text,
  first_name text,
  middle_name text,
  id_of_pulpit integer REFERENCES pulpits(id)
);

/*5*/
CREATE TABLE disciplines (
  id serial PRIMARY KEY,
  discipline text
);

/*6*/
CREATE TABLE academic_plan (
  code_of_specialty text REFERENCES specialties(code_of_specialty),
  id_of_discipline integer REFERENCES disciplines(id),
  semester integer,
  PRIMARY KEY (code_of_specialty, id_of_discipline, semester)
);

/*7*/
CREATE TABLE type_of_loads (
  short_name_of_load text PRIMARY KEY,
  full_name_of_load text
);

/*8*/
CREATE TABLE "load" (
  code_of_specialty text,
  id_of_discipline integer,
  semester integer,
  short_name_of_load text REFERENCES type_of_loads (short_name_of_load),
  count_of_hours integer,
  PRIMARY KEY (code_of_specialty, id_of_discipline, semester, short_name_of_load),
  FOREIGN KEY (code_of_specialty, id_of_discipline, semester) REFERENCES academic_plan (code_of_specialty, id_of_discipline, semester)
);

/*9*/
CREATE TABLE "time" (
  id serial PRIMARY KEY,
  "time" text,
  "session" integer CHECK ("session" = 1 OR "session" = 2)
);

/*10*/
CREATE TABLE calendar (
  id serial PRIMARY KEY,
  calendar_date date,
  number_of_week integer,
  id_of_time integer REFERENCES time(id)
);

/*11*/
CREATE TABLE type_of_auditoriums (
  short_name_of_auditorium text PRIMARY KEY,
  full_name_of_auditorium text
);

/*12*/
CREATE TABLE auditoriums (
  number_of_auditorium text PRIMARY KEY,
  capacity integer,
  short_name_of_auditorium text REFERENCES type_of_auditoriums(short_name_of_auditorium)
);

/*13*/
CREATE TABLE groups (
  code_of_specialty text REFERENCES specialties(code_of_specialty),
  number_of_group integer,
  semester integer,
  PRIMARY KEY (code_of_specialty, number_of_group, semester)
);

/*14*/
CREATE TABLE subgroups (
  code_of_specialty text,
  number_of_group integer,
  semester integer,
  number_of_subgroup integer,
  count_of_students integer,
  PRIMARY KEY (code_of_specialty, number_of_group, semester, number_of_subgroup),
  FOREIGN KEY (code_of_specialty, number_of_group, semester) REFERENCES group_table (code_of_specialty, group_num, semester)
);

/*15*/
CREATE TABLE timetable (
  id serial PRIMARY KEY,
  id_of_calendar integer REFERENCES calendar(id),
  number_of_auditorium text REFERENCES auditoriums(number_of_auditorium),
  id_of_discipline integer REFERENCES disciplines(id),
  short_name_of_load text REFERENCES type_of_loads (short_name_of_load),
  code_of_specialty text REFERENCES specialties(code_of_specialty),
  number_of_group integer,
  number_of_subgroup integer,
  semester integer,
  id_of_teacher integer REFERENCES teachers(id),
  UNIQUE (id_of_calendar, code_of_specialty, number_of_group, number_of_subgroup)
);

CREATE OR REPLACE VIEW view_timetable AS (
  SELECT timetable.id,
    calendar.calendar_date,
    time.time,
    calendar.number_of_week,
    auditoriums.number_of_auditorium,
    disciplines.name_of_discipline,
    type_of_loads.full_name_of_load,
    specialties.full_name_of_specialty,
    timetable.number_of_group,
    timetable.number_of_subgroup,
    timetable.semester,
    teachers.last_name,
    teachers.first_name,
    teachers.middle_name FROM timetable
    INNER JOIN calendar ON timetable.calendar_id = calendar.id
    INNER JOIN time ON calendar.id_of_time = time.id
    INNER JOIN auditoriums ON timetable.number_of_auditorium = auditoriums.number_of_auditorium
    INNER JOIN disciplines ON timetable.id_of_discipline = disciplines.id
    INNER JOIN specialties ON timetable.code_of_specialty = specialties.code_of_specialty
    INNER JOIN type_of_loads ON timetable.short_name_of_load = type_of_loads.short_name_of_load
    INNER JOIN teachers ON timetable.id_of_teacher = teachers.id
);

