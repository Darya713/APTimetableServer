      INSERT INTO timetable(id_of_calendar, number_of_auditorium, id_of_discipline, short_name_of_load, code_of_specialty, number_of_group, number_of_subgroup, semester, id_of_teacher)
      VALUES (_id_of_calendar, _number_of_auditorium, _id_of_discipline, _short_name_of_load, _code_of_specialty, i, j, _semester, _id_of_teacher);
    END LOOP;
  END LOOP;
END;
$$
LANGUAGE plpgsql;

