CREATE OR REPLACE FUNCTION write_to_calendar(start_date date, end_date date) RETURNS void AS $$
DECLARE
  week INTEGER;
BEGIN
  week := 1;
  LOOP
    FOR counter IN 1..7
    LOOP
      if (SELECT EXTRACT (DOW from CAST(start_date as date)) <> 0)
      then INSERT INTO calendar (calendar_date, id_of_time, number_of_week) VALUES (start_date, counter, week);
      else
        INSERT INTO calendar (calendar_date) VALUES (start_date);
        week := week + 1;
        exit;
      end if;
    END LOOP;
    start_date := start_date + 1;
    EXIT WHEN start_date > end_date;
  END LOOP;
END;
$$LANGUAGE 'plpgsql';

