CREATE OR REPLACE FUNCTION trigger_function_timetable_insert() RETURNS TRIGGER AS
$$
DECLARE i timetable%ROWTYPE;
BEGIN
  FOR i IN (SELECT timetable.number_of_auditorium, timetable.id_of_discipline, timetable.id_of_teacher FROM timetable
  WHERE timetable.id_of_calendar = NEW.id_of_calendar)
  LOOP
    IF i.number_of_auditorium = NEW.number_of_auditorium AND (i.id_of_discipline != NEW.id_of_discipline OR i.id_of_teacher != NEW.id_of_teacher)
    THEN RAISE EXCEPTION 'Auditorium is busy';
    END IF;
    IF i.id_of_teacher = NEW.id_of_teacher AND (i.id_of_discipline != NEW.id_of_discipline OR i.number_of_auditorium)
    THEN RAISE EXCEPTION 'Teacher is busy';
    END IF;
  END LOOP;
END;
$$
LANGUAGE plpgsql;

CREATE TRIGGER trigger_auditorium_insert BEFORE INSERT ON timetable FOR EACH ROW EXECUTE PROCEDURE trigger_function_timetable_insert();
