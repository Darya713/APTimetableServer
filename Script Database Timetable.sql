CREATE OR REPLACE FUNCTION timetable_select(_code_of_specialty text, _semester INTEGER, _day_of_week INTEGER)
  RETURNS TABLE (
    id text,
    id_of_class INTEGER,
    number_of_week INTEGER,
    number_of_auditorium text,
    name_of_discipline text,
    full_name_of_load text,
    full_name_of_specialty text,
    group_num INTEGER,
    subgroup INTEGER,
    semester INTEGER,
    last_name text,
    first_name text,
    middle_name text) AS
$$
BEGIN
  return QUERY SELECT
                 array_to_string(array_agg(timetable.id), ','),
                 time.id,
                 CASE WHEN mod(calendar.number_of_week, 2) = 1::integer THEN 1::integer ELSE 2::integer END as number_of_week,
                 auditoriums.number_of_auditorium,
                 disciplines.discipline,
                 type_of_loads.full_name_of_load,
                 specialties.full_name_of_specialty,
                 timetable.number_of_group,
                 timetable.number_of_subgroup,
                 timetable.semester,
                 teachers.last_name,
                 teachers.first_name,
                 teachers.middle_name FROM timetable
    INNER JOIN calendar ON timetable.id_of_calendar = calendar.id
    INNER JOIN time ON calendar.id_of_time = time.id
    INNER JOIN auditoriums ON timetable.number_of_auditorium = auditoriums.number_of_auditorium
    INNER JOIN disciplines ON timetable.id_of_discipline = disciplines.id
    INNER JOIN specialties ON timetable.code_of_specialty = specialties.code_of_specialty
    INNER JOIN type_of_loads ON timetable.short_name_of_load = type_of_loads.short_name_of_load
    INNER JOIN teachers ON timetable.id_of_teacher = teachers.id
               WHERE timetable.code_of_specialty = $1 AND timetable.semester = $2 AND extract(DOW FROM calendar.calendar_date) = $3
               GROUP BY
                 timetable.id,
                 time.id,
                 extract(DOW FROM calendar.calendar_date),
                 mod(calendar.number_of_week, 2),
                 auditoriums.number_of_auditorium,
                 disciplines.discipline,
                 type_of_loads.full_name_of_load,
                 specialties.full_name_of_specialty,
                 timetable.number_of_group,
                 timetable.number_of_subgroup,
                 timetable.semester,
                 teachers.last_name,
                 teachers.first_name,
                 teachers.middle_name;
END;
$$
LANGUAGE plpgsql;

